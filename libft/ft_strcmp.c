#include "libft.h"

int	ft_strcmp(char *a, char *b)
{
	if (ft_strlen(a) != ft_strlen(b))
		return (0);
	if (ft_strncmp(a, b, ft_strlen(b)) == 0)
		return (1);
	return (0);
}
